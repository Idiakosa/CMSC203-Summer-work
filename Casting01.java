package casting01;

public class Casting01 {
    public static void main(String[] args) {
      //  byte myByte = 128;
        
    	short myShort = 128;
    	System.out.println((byte) myShort); // cast to byte

    	byte myByte = 127;
    	System.out.println(++myByte);
    	System.out.println(++myByte);
        
    }    
}